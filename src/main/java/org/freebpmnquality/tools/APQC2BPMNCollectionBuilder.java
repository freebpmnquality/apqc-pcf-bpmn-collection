	/**
	 * Returns the list of activities by parent process ID.
	 * 
	 * @param parentProcessID - parent process ID.
	 * @return the list of activities.
	 */
	public static List<String[]> getActivityListByParentProcessId(String parentProcessID) {
		List<String[]> activityList = new ArrayList<>();

		for (String[] record : APQC_GLOSSARY) {
			String id = record[0];
			String parentId = record[2];

			if (parentProcessID.equals(parentId) && !parentProcessID.equals(id)) {
				activityList.add(record);
			}
		}

		return activityList;
	}

	/**
	 * Saves a business process model to the BPMN 2.0 file.
	 * 
	 * @param modelName    - the name of a business process model.
	 * @param title        - the title of a business process.
	 * @param activityList - the list of business process activities.
	 */
	public static void saveBPMNModel(String modelName, String title, List<String[]> activityList) {
		String activityId = activityList.get(0)[0];
		String activityName = activityList.get(0)[1];
		UserTaskBuilder builder = Bpmn.createProcess().name(title).startEvent().userTask()
				.name(activityId + " " + activityName);

		for (int i = 1; i < activityList.size(); i++) {
			activityId = activityList.get(i)[0];
			activityName = activityList.get(i)[1];
			builder = builder.userTask().name(activityId + " " + activityName);
		}

		File file = new File("models\\" + modelName + ".bpmn");
		Bpmn.writeModelToFile(file, builder.endEvent().done());
	}

	/**
	 * Creates the collection of BPMN 2.0 files.
	 */
	public static void buildBPMNCollection() {
		for (String[] record : APQC_GLOSSARY) {
			String id = record[0];
			String title = record[1].replaceAll("[^a-zA-Z0-9]", " ");
			List<String[]> activityList = getActivityListByParentProcessId(id);

			if (!activityList.isEmpty()) {
				String modelName = id + " " + title;
				System.out.println("Creating a model '" + modelName + ".bpmn' ...");
				saveBPMNModel(modelName, title, activityList);
			}
		}
	}

	public static void main(String[] args) {
		buildBPMNCollection();
	}
